/*
 * This Interface permits you to use a custom rendering engine
 * for the Captcha system.  Simply implement the only method
 * which is looking to return a java.awt.Image object that will
 * represent the final captcha.
 * 
 * The width/height are the final dimensions of the requested
 * captcha image, while the displayText is the string that is to
 * be rendered.
 */
package com.bluedragon.captcha;

import java.awt.Image;

public interface RenderImage {

	public Image renderCaptcha( RenderEngine renderEngine, String displayText, int width, int height );
	
}
